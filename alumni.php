            <p > <b>Reg. No:</b>  <br>
VAAGDEVI DEGREE & P.G COLLEGE 2-2-457/A, Kishanpura, Hanamkonda, Warangal- 506 001 (T.S) 
BY – LAWS <br>
1.0) Name and Address of the Association The name of the Association shall be “ VAAGDEVI DEGREE AND PG ALUMNI ASSOCIATION (VDAPAA) Vaagdevi Degree & P.G. College Kishanpura, Hanamkonda , Warangal – 506001. T.S.(India) <br>
2.0) The Registered Office of the Association shall be located at the Vaagdevi Degree & P.G College , Hanamkonda .(Hereafter , it is called as Vaagdevi College , Hanamkonda). • Objectives of the Association .<br>
1) To bring old Degree & PG Students of Vaagdevi Degree and P.G college, Hanamkonda under one forum for exchange of knowledge . Experience and talents among its members and also for the furtherance of fellowship and advancement of professional skills. <br>
.2) To act as liaison and support for the overall academic and infrastructural development of the various programmers offered both at UG and PG level. .<br>
3) To develop close interaction between the concerned alumni and students of various UG & PG courses in the institution. .<br>
4) To conduct Alumni Meet at least once in a year to facilitate interaction and promote friendly relations among the members. <br>
5) To organize Academic, Curricular and Co-curricular programmes with a view to develop skills and talents among the students. .<br>
6) To promote career opportunities for the alumni and the students pursuing UG & PG courses through summer placements, Campus interviews selections, Industry visits, guest lectures , projects etc., .<br>
7) To create and establish endowment for granting scholarships, prizes etc. to the UG & PG students of the institute with a view to promote and encourage their talents . <br>
.8) To raise or collect funds by subscription, Contributions donations loans or by any other legal means for the furtherance of the above objectives of the Association. .<br>
9) To publish a News Letter /Journal covering new topics of contemporary relevance. .<br>
10) To organise all such events which help personality development and soft skills among the students. <br>
.11) To undertake all such activities, deeds and things as may be incidental to the attainment of objectives of the Association . • Functions and Activities In order to fulfil the objectives,<br><br> <b>following functions and activities shall be undertaken</b><br>
3.1)Enrolment of members <br>
3.2)Forming sub- committees <br>
3.3)Organizing periodical meetings of Alumni , to plan and discuss the progress of activites . 3.4)Conducting the annual Meet of Alumni Association .<br>
3.5)Organizing various events, meets and cultural activities <br>
3.6)Publishing directory of Alumni and updating it periodically, <br>
3.7)Creating a website and communicating with members and outside world <br>
3.8)Promoting exchange of information , views etc. through group e-mail and conferencing. 3.9)To bring out News bulletin / Journal on behalf of the alumni. <br>
3.10) Organizing guest lectures , interactive sessions, workshops etc. Through alumni members or other experts from academics, for the benefit of the students and the institution.<br>
 3.11) Buildingup database of UG & PG students and providing their information relating to their placements and career growth.<br>
 3.12) Felicitating the Alumni / Teachers /Students who have achieved significant progress in their career or academic performance<br>
 3.13) Organizing sports, games, cultural activities and other social activities for the benefit of members and institution . <br>
3.14) To institute prizes , awards and scholarships to meritorious. Poor and deserving students of Vaagdevi Degree & P.G .College, Kishanpura , Hanamkonda.<br>
 4. Membership <br>
4.01) The membership of the Association is open to those who have completed their UG & PG Course in Vaagdevi Degree & P.G College . Kishanpura, Hanamkonda, Warangal.<br>
 4.02)Any one interested to join the Alumni Association can become a Patron /Donor member. Patron/Donor member will be invited to meets but can not exercise vote. <br>
4.03) The membership fee for life-member and patron /Donor member will be as follows: (1) Life Membership Rs. 500/- (2) Patron/Donar Membership Not less than Rs.1500/- 4.04) for the purpose of co-ordination and effective implementation of Alumni Programmes , the present students of Vaagdevi Degree & P.G College, Kishanpura, Hanamkonda, Warangal. Automatically become Associate Members. The Associate Members shall not have any voting rights. <br>
5. Organisation<br>
 5.01) The Alumni Association will be managed through an Executive committee which consists of the following positions: <br>
1) President (Ex – Officio Member) : The Principal of Vaagdevi Degree & P.G college, Kishanpura , Hanamkonda.<br>
 2)Vice –President : Aca.co <br>
3)General Secretary : Elected by General Body <br>
4)Joint Secretary : Elected by General Body <br>
5)Treasures: Elected by General Body <br>
6)Executive Members will be Five Elected by General Body.<br>
 5.02) The Secretary and Correspondent of Vaagdevi Degree & P.G College , Shall be the Chief- patron of the Association and provide guidance and support to the alumni activities.<br>
 6.0) Functions of the Office bearers <br>
A. President : The President shall presideover the meetings and acts as a Chief- Executive officer of the Association . <br>
B. Vice –President : The Vice-President shall assist the President in Discharging functions. In the absence of the President vice-president, he will Preside-over the General Body and Executive Committee. <br>
C. General Secretary: (i) The General Secretary shall be the custodian of all records related to the Association. He is responsible for all types of correspondence on behalf of the Association.<br>
 (ii) General Secretary shall be the custodian of all records related to the Association and convene both the Executive Committee and General Body of the Association with permission of the President. The General Secretary shall guide the Treasurer in preparing the annual accounts, budget and income and expenditure statements to be placed before the General Body for its approval .He is elected by the General Body. <br>
(iii) In the absence of the Treasurer , he will act as a Treasurer with the approval of the Executive Committee. D. Joint – Secretary : The Joint – Secretary shall do the work entrusted by Executive Committee and assist the General Secretary in discharging the duties. In the absence of General Secretary , the Joint Secretary shall perform the duties of the General Secretary. E. Treasurer : The Treasurer is responsible for all financial transactions and funds of the Association and shall maintain accounts properly along with the vouchers. The treasurer shall prepare the accounts of the Association and operate the bank account jointly with the President/Vice-President.He shall get the accounts audited and submit them to the General Body of the Association. <br>
7.0) General Body A): (i) The General Bony shall consist of life members.<br>
 (ii) The General Body Shall meet at least once in a year .The Executive Committee is empowered to convene the General Body in case of any urgency or on the written requisition made by one third of the members .<br>
 B) Functions: (i) To pass the budget for ensuing year and approve the income and expenditure statement and balance sheet of the previous year. <br>
(ii) To approve the reports and the activities of the Association. <br>
(iii) To elect the office bearers and the Executive Committee and <br>
(iv) To appoint and renew the appointment of the Auditor. <br>
8.0) Meetings : 8.01) There shall be two types of meetings conducted by the Alumni Association. They are. a) Annual General Body Meeting : Annual General Body Meeting shall be conducted at least once in a year. However, in emergency General Body Meeting may be convened by the Executive Committee on written requisition made by 1/3 of the members . <br>
b) Executive Committee Meeting : It is mandatory to hold the Executive Committee meeting at least once between two general body Meeting to discuss the activities of the Alumni Association. The President shall preside over the Executive committe meeting. <br>
9.0) Term of Office : <br>
9.01) The tenture of the President; Vice –President; General Secretary; Joint Secretary; Treasurer and nominated members of the Executive Committee shall be for two years from the date of assuming charge. <br>
9.02) No elected member shall hold the same position for more then two(2) terms and for not more than four(4) terms in all positions put together . <br>
10.0) Quorum :<br>
 10.01) The quorum for the meeting of the General Body shall be 2/3 of the total membership or 20 whichever is lower.<br>
 10.2) The quorum for the meetings of the Executive Committee shall 1/3 or 4 whichever is lower. <br>
11.0) Funds: <br>
11.01) The membership fee is the main source of funds. The mobilised through life membership shall be deposited in the form of fixed deposit in any nationalized bank. The interest accured shall be used for undertaking activities. The Association can accept donations, sponsorships from individual, business houses etc., for pursuing its objectives. It can also receive fee for consulation services / training programmes, if any. <br>
11.02) Funds should be used for the activities of the Alumni Association in the spirit of attaining 12.0) Accounts and Audit :<br>
 12.01) The following books shall be maintained in the office of the Alumni Association.<br>
 <ul> 
    <li> Cash book</li>
 <li> Ledger</li>
<li> Stock Register </li>
<li> Members book</li>
<li> Minutes book</li>
 <li> Assets book</li></ul>
 
12.02) The bank account must be operated by two signatories. Cheque should be signed by the President /Vice - President and Treasurers signature is compulsory. In the absence of the Treasurer , the General Secretary will act as Treasurer, with the approval of the Executive Committee. <br>
12.03) The Annual accounts of the Association consists of: 
<ul> 
    <li> Receipt and Payments Account</li>
 <li> Ledger</li>
<li>  Income and Expenditure Account </li>
<li>  Balance Sheet The Treasurer is responsible to prepare annual accounts and submit to the Annual General Meeting of the Alumni.</li>
</ul>  
12.04) The accounts will be audited every year by an external auditor appointed by the General Body. <br>
13) Amendments : No amendment or alteration shall be made in the by laws of the Association unless it is approved by 2/3 of the members present in the General body meeting. <br>
14) Winding up: In case the Association has to be wounded up , property and fund of the Association that remain after discharging the liabilities, if any, shall be transferred to Vaagdevi Degree & P.G. College , Kishanpura, Hanamkonda, Warangal .<br>


