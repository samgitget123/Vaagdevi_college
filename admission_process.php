                                    <p>DOST offers single window (DOST website https://dost.cgg.gov.in) for seeking admission into any of the undergraduate programs in any of the state universities (Osmania, Kakatiya, Telangana, Palamuru, Mahatma Gandhi, Sathavahana Universities). The process of admission is simple. It is student friendly and one can do it by himself/herself. Candidate must register himself/herself by visiting the website and logging in by the Intermediate Hall -ticket number. 
