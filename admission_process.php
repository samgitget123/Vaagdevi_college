                                    <h4>➢DOST-2021 Admission Process for the students </h4>
                                    <p><b>Method-I:</b>  If a student has already linked Aadhaar number with the mobile number, he/she can directly register in DOST Website with mobile OTP authentication. 
                    In case, his/her Aadhaar number is not seeded with mobile number, the students shall link his or his parent’s mobile number to the student’s Aadhaar at Aadhaar Update Centres. 
                    </p>
                                    <p><b>Method-II:</b> The students visit MeeSeva Centre for biometric authentication and registration in DOST. </p>
                                    <p><b> Method-III:</b> The <b>TSBIE</b>  Students can register in DOST through <b>T App Folio Mobile App</b>  based photo authentication.  </p>

                                    <ul>
                    <li>	The students may install Mobile based T App Folio which contains DOST ID generation service. </li>
                    <li>	Students shall enter Hall ticket Number of TSBIE, Date of Birth, Aadhaar Number and Mobile Number.</li> 
                    <li>	On verification of Hall Ticket, Date of Birth, Uniqueness of Aadhaar and Uniqueness of Mobile, the details of the data (Candidate’s Name, Father’s Name, Mother’s Name, Gender, Candidate’s Photograph) would be returned to TS App Folio application. </li>
                    <li>    On successful authentication of live photograph (selfie) with the photograph available in TSBIE service, DOST Reference ID is generated. </li>
                    <li>	Students get DOST Reference ID over SMS and on App. </li>
                    <li>	The students can go for further process of registration and exercise web options on DOST online web portal. </li>
                    <li>	Registration on DOST is by paying registration fee of Rs 200/- </li>
                    <li>	After the registration students will get DOST ID and PIN. The students are advised to keep their DOST ID and PIN carefully and confidentially till the end of the admission process. </li>
                    <li>	The students shall log in using DOST ID and PIN/password to open the Application Form. </li>
                    <li>	They must fill in the application form with all the correct details that are required (once the data is submitted it cannot be edited). </li>
                    <li>	Then they must exercise web options by choosing the course and college of their preference by giving the priority numbers. (One must be careful while giving college and course priorities as the seats will be allotted according to the priorities specified in the options.) </li>
                    <li>	The students are advised not to share DOST ID/PIN/Password with anybody to keep their options confidential. </li>
                    <li>	Seats will be allotted based on the merit and reservations in force. </li>
                    <li>	If the students are satisfied with the seat allotted, they must confirm the seat by online self-reporting and pay the required confirmation fee online as per the payment mentioned below. </li>
                    <li>	The students who confirm their seat (in any phase) by online self-reporting must visit the allotted college in person from 18.08.2021 to 21.08.2021 and submit the required certificates and pay fee, then only your seat is confirmed. </li>
                    <li>	If the student is not satisfied with the seat secured, he can pay fee (online payment) for the reservation of the seat and then, go for web options once again in the second and third phases. </li>

                                    </ul>


