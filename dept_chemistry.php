            <h4 class="mt-3">Vision: </h4>
            <p>To be a successful model for education, research and innovation in the chemical sciences. </p>

            <h4 class="mt-3">Mission: </h4>
            <p>1. Provide distinctive and appropriate education in the chemical sciences at the Post graduate and graduate levels. 
2. Carry out world-class research and scholarly work with relevance to contemporary problems of society and industry. QUALITY POLICY Department of Chemistry is committed to deliver quality education for all-round development of the students to meet the changing requirements of industry, business and society.
</p>
            <h4>PROGRAME EDUCATIONAL OBJECTIVES: </h4>
            <p>(PEO’s): 1. PEO-1: To prepare students for successful practice in diverse fields of Chemical Sciences such as pharmaceutical, chemical, polymer / advanced material, energy, biotechnology and environmental engineering and in the fields of Societal expectations on time. <br>
2. PEO-2: To prepare students for advanced studies in Chemical sciences and its allied fields. <br>
3. PEO-3: To ensure our students to achieve excellence and get selected for high-ranking industrial, academic, Government and other professional positions, as well as to inculcate leadership qualities. <br>
4. PEO-4: To develop graduate’s skills and awareness to become socially, ethically and morally responsible individual in all the challenges they take over, in our communities and in the field of chemical Sciences.<br>
</p>
<p> <b>Programmes held:</b>  Department of Chemistry offered Under Graduate programme (BSc) as Chemistry one of the optional along with Bio technology, Bio Chemistry, Micro biology, Food and Nutrition, Zoology, Botany, Computer science, Physics and Computer Applications for the benefit of the students and to get bright carriers with their Bachelor degree. During this programme we deliver principles of chemistry in the mode of theory and practicals with at most perfection, proper motivation which can be helpful to the students to get placements in MNC s like Dr Reddy, Hetero, Cipla, Ranbaxy, Rankem..Etc. Every year so many of our students selected in Placement drive which are conducted at our college campus and outside also. In part of our academic curriculum, Department of chemistry offered 2 years Post graduation programme M.sc (Organic chemistry) since 2000, especially we provided practical based education by involving the students in various Govt of India funded Research projects(Human Resource Development Group, Ministry of Science and Technology and Council of Scientific and Industrial Research India),conducting Industrial tours, Interaction programmes with eminent and efficient personalities of Chemistry along with this we sponsored the students to attend National and International conferences in India as well as abroad also with the help of International students exchange programmes, Summer Research fellowship and Internship at CSIR-Indian Institute of Chemical Technology, Hyderabad which are helpful to their carrier.<br><br><b>Department Facilities:</b>  We are committed to the philosophy that students and faculty need access to the most current techniques and instruments, and we receive strong extramural funding to update equipment as the science evolves. So we established an internationally renowned Academic and Research laboratories, the department of chemistry provides an unparalleled support network for graduate and Post graduate research.</p>
